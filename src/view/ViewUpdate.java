    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE tb_user SET nome = ?, cpf = ?, rg = ?, dataNasc = ? WHERE id = ?";
        String nome = txtNome.getText();
        String cpf = txtCPF.getText().replaceAll("[^0-9]", ""); // Remove caracteres não numéricos do CPF
        String rg = txtRG.getText();
        String dataNasc = txtData.getText();
        User user = new User();
        user.setId(userId);
        user.setNome(nome);
        user.setCpf(cpf);
        user.setRg(rg);
        user.setDataNasc(dataNasc);
        try (Connection conn = conexao.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, user.getNome());
            pstmt.setString(2, user.getCpf());
            pstmt.setString(3, user.getRg());
            java.sql.Date sqlDate = new java.sql.Date(user.getDataNasc().getTime());
            pstmt.setDate(4, sqlDate);
            pstmt.setInt(5, user.getId());

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso!");
            this.dispose();
            telaInicial.atualizarTabela();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar o usuário: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao conectar ao banco de dados: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed


